def area(width, height):
    result = width * height
    return result
result = area(5,6)
print(result)
result = area(7, 9)
print(result)
result = area(12, 12)
print(result)

def subtract(num1, num2):
    result = num1 - num2
    return result
result = subtract(20, 10)
print(result)
result = subtract(50, 30)
print(result)

def add(num1, num2):
    result = num1 + num2
    return result
result = add(10, 2)
print(result)
result = add(100, 5)
print(result)
